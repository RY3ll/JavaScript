let n1 = 23746
let n2 = 521
let op = '-'

if (op == '*') {
    console.log(`${n1} * ${n2} = ${n1 * n2}`)
}

if (op == '+') {
    console.log(`${n1} + ${n2} = ${n1 + n2}`)
}

if (op == '-') {
    console.log(`${n1} - ${n2} = ${n1 - n2}`)
}

if (op == '/') {
    console.log(`${n1} / ${n2} = ${n1 / n2}`)
}